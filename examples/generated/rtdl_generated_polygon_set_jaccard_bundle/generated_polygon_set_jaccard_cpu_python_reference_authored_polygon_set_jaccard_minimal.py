from __future__ import annotations

import json
import sys
from pathlib import Path


def _find_repo_root(start: Path) -> Path:
    for candidate in (start, *start.parents):
        if (candidate / "src" / "rtdsl" / "__init__.py").exists():
            return candidate
    raise RuntimeError("could not locate RTDL repo root from generated program path")


ROOT = _find_repo_root(Path(__file__).resolve().parent)
sys.path.insert(0, str(ROOT / "src"))
sys.path.insert(0, str(ROOT))

import rtdsl as rt

REQUEST_WORKLOAD = "polygon_set_jaccard"
REQUEST_DATASET = "authored_polygon_set_jaccard_minimal"
REQUEST_BACKEND = "cpu_python_reference"
REQUEST_VERIFY = True
REQUEST_OUTPUT_MODE = "rows"


@rt.kernel(backend="rtdl", precision="float_approx")
def generated_polygon_set_jaccard():
    left = rt.input("left", rt.Polygons, layout=rt.Polygon2DLayout, role="probe")
    right = rt.input("right", rt.Polygons, layout=rt.Polygon2DLayout, role="build")
    candidates = rt.traverse(left, right, accel="bvh")
    rows = rt.refine(candidates, predicate=rt.polygon_set_jaccard(exact=False))
    return rt.emit(
        rows,
        fields=[
            "intersection_area",
            "left_area",
            "right_area",
            "union_area",
            "jaccard_similarity",
        ],
    )


def build_case() -> dict[str, object]:
    if REQUEST_DATASET == "authored_polygon_set_jaccard_minimal":
        return {
            "left": (
                rt.Polygon(id=1, vertices=((0.0, 0.0), (3.0, 0.0), (3.0, 3.0), (0.0, 3.0))),
                rt.Polygon(id=2, vertices=((4.0, 0.0), (6.0, 0.0), (6.0, 2.0), (4.0, 2.0))),
            ),
            "right": (
                rt.Polygon(id=10, vertices=((1.0, 1.0), (4.0, 1.0), (4.0, 4.0), (1.0, 4.0))),
                rt.Polygon(id=11, vertices=((5.0, 0.0), (7.0, 0.0), (7.0, 1.0), (5.0, 1.0))),
            ),
        }
    raise ValueError(f"unsupported generated dataset `{REQUEST_DATASET}`")


def run_backend(case_inputs: dict[str, object]):
    if REQUEST_BACKEND == "cpu_python_reference":
        return rt.run_cpu_python_reference(generated_polygon_set_jaccard, **case_inputs)
    if REQUEST_BACKEND == "cpu":
        return rt.run_cpu(generated_polygon_set_jaccard, **case_inputs)
    raise ValueError(f"unsupported generated backend `{REQUEST_BACKEND}`")


def verify_rows(case_inputs: dict[str, object], actual_rows) -> bool:
    expected_rows = rt.run_cpu_python_reference(generated_polygon_set_jaccard, **case_inputs)
    return expected_rows == actual_rows


def main() -> int:
    case_inputs = build_case()
    rows = run_backend(case_inputs)
    verified = verify_rows(case_inputs, rows) if REQUEST_VERIFY else None
    payload = {
        "workload": REQUEST_WORKLOAD,
        "dataset": REQUEST_DATASET,
        "backend": REQUEST_BACKEND,
    }
    if REQUEST_VERIFY:
        payload["verified_against_cpu_python_reference"] = verified
    if REQUEST_OUTPUT_MODE == "summary":
        payload["row_count"] = len(rows)
    else:
        payload["rows"] = rows
    print(json.dumps(payload, indent=2, sort_keys=True))
    if verified is False:
        raise SystemExit("generated program verification failed")
    return 0


if __name__ == "__main__":
    # Generated by RTDL generate-only support for the narrow Jaccard aggregate workload.
    raise SystemExit(main())
